#include "libft.h"

int			ft_count_words(char const *s, char del)
{
	int		i;
	int		g;

	i = 0;
	g = 0;
	while (s[i] != '\0')
	{
		if (s[i] != del)
		{
			g++;
			while (s[i] != del && s[i + 1] != '\0')
				i++;
		}
		i++;
	}
	return (g);
}
