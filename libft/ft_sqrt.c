#include "libft.h"

double	ft_sqrt(double nb)
{
	double i;

	i = 1;
	if (nb > 0)
	{
		while (i < nb)
		{
			if ((i * i) == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
